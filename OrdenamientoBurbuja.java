    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        if (arreglo != null) {
            for (int i = 0; i < arreglo.length; i++) {
                arreglo[i] = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el número en la posición " + i + ":"));
            }
            JOptionPane.showMessageDialog(null, "Números ingresados manualmente.");
        } else {
            JOptionPane.showMessageDialog(null, "Primero defina el tamaño del arreglo.");
        }
    }                                        

