    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        int tamaño = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el tamaño del arreglo:"));
        arreglo = new int[tamaño];
        JOptionPane.showMessageDialog(null, "Tamaño del arreglo definido a " + tamaño);
    }                                        

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        if (arreglo != null) {
            int n = arreglo.length;
            for (int i = 0; i < n - 1; i++) {
                for (int j = 0; j < n - i - 1; j++) {
                    if (arreglo[j] > arreglo[j + 1]) {
                        // Intercambiar los elementos si estan en el orden incorrecto
                        int temp = arreglo[j];
                        arreglo[j] = arreglo[j + 1];
                        arreglo[j + 1] = temp;
                    }
                }
            }
            JOptionPane.showMessageDialog(null, "Arreglo ordenado con éxito.");
        } else {
            JOptionPane.showMessageDialog(null, "Primero defina el tamaño del arreglo.");
        }

    }                                        

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        if (arreglo != null) {
            StringBuilder resultado = new StringBuilder("Arreglo ordenado:\n");
            int n = arreglo.length;
            for (int i = 0; i < n; i++) {
                resultado.append(arreglo[i]);
                if (i < n - 1) {
                    resultado.append(", ");
                }
            }
            JOptionPane.showMessageDialog(null, resultado.toString());
        } else {
            JOptionPane.showMessageDialog(null, "El arreglo no ha sido definido o generado.");
        }
    }                                        

